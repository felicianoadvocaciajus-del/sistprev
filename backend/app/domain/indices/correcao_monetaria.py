@lru_cache(maxsize=4096)
def fator_acumulado(competencia_inicio: Tuple[int, int],
                    competencia_fim: Tuple[int, int]) -> Decimal:
    """
    Calcula o fator acumulado de correção monetária entre duas competências.

    O fator é o PRODUTO de todos os índices mensais de competencia_inicio
    até competencia_fim EXCLUSIVE (a competência da DER não é corrigida).

    Exemplo: para corrigir o salário de jan/2000 até a DER de dez/2010,
    multiplica-se todos os índices de fev/2000 até dez/2010.

    Retorna Decimal. NUNCA converte para float.
    """
    ai, mi = competencia_inicio
    af, mf = competencia_fim

    inicio = date(ai, mi, 1)
    fim = date(af, mf, 1)

    if inicio == fim:
        return _D("1")

    if inicio > fim:
        # Periodo invertido: retorna o inverso (fator < 1).
        # Util para "trazer ao valor presente" um valor futuro.
        fator_normal = _fator_acumulado_simples(fim, inicio)
        if fator_normal == 0:
            return _D("1")
        return (_D("1") / fator_normal).quantize(
            _D("0.000000001"), rounding=ROUND_HALF_UP
        )

    return _fator_acumulado_simples(inicio, fim).quantize(
        _D("0.000000001"), rounding=ROUND_HALF_UP
    )


def _fator_acumulado_simples(inicio: date, fim: date) -> Decimal:
    """Produto dos indices de inicio (exclusive) ate fim (inclusive)."""
    fator = _D("1")
    atual = _next_month(inicio)
    while atual <= fim:
        key = (atual.year, atual.month)
        indice = CADEIA_INDICES.get(key)
        if indice is None:
            indice = _D("1")
        fator = fator * indice
        atual = _next_month(atual)
    return fator


def corrigir_salario(salario: Decimal,
                     competencia_salario: date,
                     der: date) -> Decimal:
    """
    Corrige um salário de contribuição da sua competência até a DER.

    salario: valor nominal do salário de contribuição
    competencia_salario: mês de referência do salário (dia sempre = 1)
    der: Data de Entrada do Requerimento (mês anterior é o último corrigido)

    Retorna o salário corrigido (Decimal, 2 casas decimais).
    """
    if not isinstance(salario, Decimal):
        salario = Decimal(str(salario))

    c_inicio = (competencia_salario.year, competencia_salario.month)
    c_fim = (der.year, der.month)

    fator = fator_acumulado(c_inicio, c_fim)
    corrigido = salario * fator
    return corrigido.quantize(_D("0.01"), rounding=ROUND_HALF_UP)


def _next_month(d: date) -> date:
    if d.month == 12:
        return date(d.year + 1, 1, 1)
    return date(d.year, d.month + 1, 1)


def indices_disponiveis() -> list:
    """Retorna lista de todas as competências com índice disponível."""
    return sorted(CADEIA_INDICES.keys())


def indice_do_mes(ano: int, mes: int) -> Optional[Decimal]:
    """Retorna o índice mensal de uma competência específica."""
    return CADEIA_INDICES.get((ano, mes))
