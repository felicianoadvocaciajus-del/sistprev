_ANOS_DISPONIVEIS = sorted(EXPECTATIVA_IBGE.keys())


@lru_cache(maxsize=4096)
def expectativa_sobrevida(idade_anos: int, ano_referencia: int) -> Decimal:
    """
    Retorna a expectativa de sobrevida em anos para uma idade e ano de referência.

    idade_anos: idade inteira do segurado na data da aposentadoria
    ano_referencia: ano em que a aposentadoria é requerida (tabela IBGE publicada para o ano)

    Se a idade não constar na tabela, usa interpolação linear entre as entradas vizinhas.
    Se o ano não constar, usa o mais recente disponível.
    """
    # Selecionar a tabela do ano
    tabela_ano = ano_referencia
    if ano_referencia not in EXPECTATIVA_IBGE:
        # Usar tabela do ano mais próximo disponível
        disponiveis = [a for a in _ANOS_DISPONIVEIS if a <= ano_referencia]
        if disponiveis:
            tabela_ano = max(disponiveis)
        else:
            tabela_ano = _ANOS_DISPONIVEIS[0]

    tabela = EXPECTATIVA_IBGE[tabela_ano]

    # Buscar a expectativa para a idade
    if idade_anos in tabela:
        return tabela[idade_anos]

    # Limitar ao intervalo disponível
    idades = sorted(tabela.keys())
    if idade_anos < idades[0]:
        return tabela[idades[0]]
    if idade_anos > idades[-1]:
        return tabela[idades[-1]]

    # Interpolação linear entre dois pontos
    for i in range(len(idades) - 1):
        if idades[i] <= idade_anos <= idades[i + 1]:
            i1, i2 = idades[i], idades[i + 1]
            e1, e2 = tabela[i1], tabela[i2]
            frac = Decimal(str(idade_anos - i1)) / Decimal(str(i2 - i1))
            return e1 + (e2 - e1) * frac

    return tabela[idades[-1]]
